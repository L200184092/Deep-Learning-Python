#contoh pengaturan pemisah pada print()

x=21
y=45

print("dua nilai dalam baris berbeda");
print(x)
print(y)

print("dua nilai dalam baris yang sama");
print("dengan pemisah berupa satu spasi");
print(x, end = ' ')
print(y)

print("dua nilai dalam baris yang sama");
print("dengan pemisah berupa *");
print(x, end = '*')
print(y)

print("nilai dalam baris yang sama");
print("tanpa pemisah");
print(x, end = '')
print(y)
